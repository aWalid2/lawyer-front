export const CHAT_BOT_PROMPT_SUGGESTIONS = [
  "اكتب لي مسودة رد قانوني مختصر على مطالبة مالية.",
  "لخص لي الخطوات اللازمة لتجهيز ملف قضية جديدة داخل المكتب.",
  "ما البيانات التي يجب مراجعتها قبل جلسة الاستئناف؟",
  "اكتب رسالة رسمية للعميل لطلب مستندات ناقصة.",
];

const WELCOME_REPLY =
  "أنا مساعد مكتب قانوني. أقدر أساعدك في الصياغة، التلخيص، تنظيم الخطوات، وتحويل أفكارك إلى ردود ورسائل قانونية عملية.";

export const generateAssistantReply = (
  prompt: string,
  attachmentsCount = 0,
) => {
  const normalizedPrompt = prompt.toLowerCase();
  const attachmentNote = attachmentsCount
    ? `\n\nتم استلام ${attachmentsCount} مرفق، ويمكنك أن تطلب مني التلخيص أو استخراج النقاط الرئيسية منه.`
    : "";

  if (
    normalizedPrompt.includes("عقد") ||
    normalizedPrompt.includes("contract")
  ) {
    return `يمكنك العمل على مراجعة العقد بهذه الخطوات:\n\n1. التأكد من بيانات الأطراف وصفاتهم القانونية.\n2. مراجعة محل الالتزام وقيمته ومدته.\n3. فحص الشرط الجزائي ووسائل الإنهاء والفسخ.\n4. التأكد من الاختصاص القضائي وآلية الإخطار.\n5. توثيق البنود الغامضة وطلب تعديلها بصياغة محددة.${attachmentNote}`;
  }

  if (
    normalizedPrompt.includes("جلسة") ||
    normalizedPrompt.includes("استئناف") ||
    normalizedPrompt.includes("appeal")
  ) {
    return `قبل الجلسة راجع هذا الترتيب:\n\n1. رقم القضية والدائرة وتاريخ الجلسة.\n2. آخر إجراء تم في الملف وما إذا كانت هناك مذكرات مطلوبة.\n3. المستندات الأساسية وأي نواقص تحتاج استكمال.\n4. دفوع الخصم والردود المختصرة عليها.\n5. الطلبات الختامية التي سيجري التمسك بها أمام المحكمة.${attachmentNote}`;
  }

  if (
    normalizedPrompt.includes("رسالة") ||
    normalizedPrompt.includes("email") ||
    normalizedPrompt.includes("عميل")
  ) {
    return `صيغة مناسبة يمكنك البدء منها:\n\nالسلام عليكم،\n\nنفيدكم بأننا نراجع الملف حالياً، ونرجو تزويدنا بالمستندات أو البيانات الناقصة حتى نستكمل الإجراء في الوقت المناسب.\n\nيرجى إرسال المستندات التالية: \n- صورة الهوية أو السجل\n- المستندات المؤيدة للواقعة\n- أي مراسلات أو إشعارات سابقة\n\nمع التحية.${attachmentNote}`;
  }

  if (normalizedPrompt.includes("تلخيص") || normalizedPrompt.includes("لخص")) {
    return `للتلخيص العملي، أرسل لي النص أو المستند واطلب أحد هذه المخرجات:\n\n1. ملخص تنفيذي قصير.\n2. الوقائع مرتبة زمنياً.\n3. الطلبات والدفوع الرئيسية.\n4. قائمة بالمخاطر أو النقاط الناقصة.${attachmentNote}`;
  }

  return `${WELCOME_REPLY}\n\nبناءً على طلبك، أقترح أن نرتب العمل بهذه الصورة:\n\n1. تحديد المطلوب النهائي بدقة.\n2. جمع الوقائع والمستندات المرتبطة.\n3. صياغة الرد أو المذكرة أو الرسالة بشكل مختصر وواضح.\n4. مراجعة الأسماء والتواريخ والأرقام قبل الاعتماد النهائي.${attachmentNote}`;
};